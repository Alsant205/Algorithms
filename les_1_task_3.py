"""
3. По введенным пользователем координатам двух точек вывести уравнение
прямой вида y = kx + b, проходящей через эти точки.
"""
print('Задайте координаты точки А:')
ax = float(input())
ay = float(input())
print('Задайте координаты точки В:')
bx = float(input())
by = float(input())

k = (ay - by) / (ax - bx)
b = by - k * bx

print(f'\nУравненеие прямой с координатами:\n'
      f'А{ax, ay} и В{bx, by}:\n'
      f'y = {k:.2} * x - {b:.2}')
